// Basic assignment operator
var value = 0

// Addition assignment operator
value = value + 10
value += 10

// Subraction assignment operator
value = value - 5
value -= 5

// Multiplication assignment operator
value = value * 2
value *= 2

// Division assignment operator
value = value / 2
value /= 2

// Modulo assignment operator
value = value % 2
value %= 2

