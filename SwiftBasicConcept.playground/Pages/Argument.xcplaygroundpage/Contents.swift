/*:
 ## Argument vs Parameter
 - Parameter
 - 매개변수 = 인자 = Parameter
 - 인수를 담는 변수의 한 종류로서 해당 함수 내부 스코프에만 영향
 - 대부분 call by value 가 기본
 - call by reference 는 호출할 때 사용한 전달인자에까지 영향
 
 - Argument
 - 전달인자 = 인수 = 실인수 = Argument
 - 함수 호출 시 그에 필요한 데이터를 전달
 */


//
//PARAMETER란? '매개변수'란 뜻으로 외부로부터 받아들이는 임의의 값을 의미한다.
//
//function f(x,y){
//    return x+y;
//};
//에서 x,y가 파라미터라고 할수 있다.
//
//ARGUMENT란? 우리말로는 '인수' 라고 번역되는데, 함수를 호출할 때 사용하게 되는 일련의 값
//함수를 호출한다고 하면,
//f(3,4);
//에서 3,4등이 아규먼트이다.
//
//즉, '파라미터의 값으로 아규먼트 3과 4를 대입하였다' 라는 의미가 성립하는것이다.
//분명 유사하지만 다른 값이다.


