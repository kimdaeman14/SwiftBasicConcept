/*:
 ---  
 ## Type Inference ( 타입 추론 )
 * 변수 선언 시 초기화로 사용되는 값의 타입을 통해 변수의 타입을 추론하여 적용하는 것
 ---
 */

let name = "Tori" //값이 토리니까 문자열 자료형으로 인식
type(of: name)

let age = 4 //인트 
type(of: age)

var weight = 6.4 //더블
type(of: weight)

var spelling = ["T", "O", "R", "I"] //배열
type(of: spelling)

